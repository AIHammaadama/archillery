<script>
    function vendorForm() {
        return {
            selectedState: '<?php echo e(old("state_id", $vendor->state_id)); ?>',
            selectedLga: '<?php echo e(old("lga_id", $vendor->lga_id)); ?>',
            lgas: <?php echo json_encode($lgas ?? [], 15, 512) ?>,
            lgasLoading: false,

            init() {
                if (this.selectedState && this.lgas.length === 0) {
                    this.loadLgas();
                }
            },

            async loadLgas() {
                if (!this.selectedState) {
                    this.lgas = [];
                    this.selectedLga = '';
                    return;
                }

                this.lgasLoading = true;
                try {
                    const response = await fetch(`/api/states/${this.selectedState}/lgas`);
                    if (response.ok) {
                        this.lgas = await response.json();
                    }
                } catch (error) {
                    console.error('Failed to load LGAs:', error);
                } finally {
                    this.lgasLoading = false;
                }
            }
        }
    }
</script>
